letra = input("Digite a letra desejada:\n")
letra = letra.upper()

if letra == "A"\
or letra == "E"\
or letra == "I"\
or letra == "O"\
or letra == "U":
    print(f"{letra} é Vogal")
else:
    print(f"{letra} é Consoante")